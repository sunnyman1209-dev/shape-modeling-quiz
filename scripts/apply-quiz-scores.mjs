/**
 * quiz_scores 테이블 생성 (로컬 1회)
 * 필요: .env.local 의 DATABASE_URL
 */
import { existsSync, readFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";
import pg from "pg";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const envPath = join(root, ".env.local");

function loadDotenv(p) {
  if (!existsSync(p)) return;
  const text = readFileSync(p, "utf8");
  for (const line of text.split("\n")) {
    const m = line.match(/^\s*([A-Za-z_][A-Za-z0-9_]*)\s*=\s*(.*)$/);
    if (!m) continue;
    let v = m[2].trim();
    if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) {
      v = v.slice(1, -1);
    }
    if (!process.env[m[1]]) process.env[m[1]] = v;
  }
}

loadDotenv(envPath);

const url = process.env.DATABASE_URL?.trim() || process.env.SUPABASE_DB_URL?.trim();
if (!url) {
  console.error(`
[apply-quiz-scores] DATABASE_URL 이 없습니다.

1) Supabase → Project Settings → Database
2) Connection string → URI
3) 프로젝트 루트에 .env.local 을 만들고 (.env.local.example 참고)

DATABASE_URL=postgresql://...

그다음: npm run db:quiz
`);
  process.exit(1);
}

const statements = [
  `create table if not exists public.quiz_scores (
  id bigint generated by default as identity primary key,
  student_id text not null,
  student_name text not null,
  score integer not null check (score >= 0 and score <= 100),
  subject text not null default '형상모델링검토',
  submitted_at timestamptz not null default now()
)`,
  `create index if not exists quiz_scores_subject_idx on public.quiz_scores (subject)`,
  `create index if not exists quiz_scores_student_id_idx on public.quiz_scores (student_id)`,
  `alter table public.quiz_scores enable row level security`
];

const client = new pg.Client({
  connectionString: url,
  ssl: url.includes("localhost") ? false : { rejectUnauthorized: false }
});

try {
  await client.connect();
  for (const sql of statements) {
    await client.query(sql);
  }
  console.log("[apply-quiz-scores] OK — quiz_scores 테이블 준비됨");
} catch (e) {
  console.error("[apply-quiz-scores] 실패:", e.message);
  process.exit(1);
} finally {
  await client.end();
}
